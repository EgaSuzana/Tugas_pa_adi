//looping for
for (let i = 1; i <= 5; i++) {
    console.log(`${i}. saya pasti bisa gaskeunn`);  
}
//looping while
let a = 1;
while (a <= 5) {
    console.log(`${a}. saya pasti bisa gaskeun`);
    a++;
}
//looping do while
let x = 1;
do {
    console.log(`${x}. saya pasti bisa gaskeun`);
    x++;
} while (x <= 5);